package Day1226;

public class CountReview1 {
	private String name;
	private int st;
	private int nd;
	
	public CountReview1()
	{
		
	}

	public CountReview1(String name, int st, int nd) {
		super();
		this.name = name;
		this.st = st;
		this.nd = nd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSt() {
		return st;
	}

	public void setSt(int st) {
		this.st = st;
	}

	public int getNd() {
		return nd;
	}

	public void setNd(int nd) {
		this.nd = nd;
	}
	
	
	
}
